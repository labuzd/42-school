#include <unistd.h>

void ft_reverse(char *s)
{
	int i;

	i = 0;
	while (s[i])
		++i;
	--i;
	while (i >= 0)
	{
		write(1, s + i, 1);
		--i;
	}
}

int main(int ac, char **av)
{
	if (ac == 2)
		ft_reverse(av[1]);
	write(1, "\n", 1);
	return (0);
}

