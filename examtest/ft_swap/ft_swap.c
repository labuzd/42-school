void	ft_swap(int *a, int *b)
{
	int swap;

	swap = *a;
	*a = *b;
	*b = swap;
}

#include <stdio.h>
int		main()
{
	int a = 10;
	int b = 5;

	ft_swap(&a, &b);
	printf("a = %d, b = %d", a, b);
}
